package com.boarz.auth.jwt;

public class RoleWrapper {

    private long id;
    private String name;

    public RoleWrapper(long id, String name) {
        this.id = id;
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
